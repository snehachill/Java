public class BitwiseOperator{
    public static void main(String[] args) {
        int a=5;
        int b=6;
        System.out.println(a & b);//AND operator
        System.out.println(a | b);//OR operator
        System.out.println(a ^ b);//XOR operator
        System.out.println(~ a);//1's compliment
        System.out.println(5<<2);//left-shift
        System.out.println(5>>2);//right-shift

    }
}